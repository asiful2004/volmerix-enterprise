class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('lang') || this.detectLanguage();
        this.init();
    }

    detectLanguage() {
        // Detect Korean users
        const userLang = navigator.language || navigator.userLanguage;
        return userLang.startsWith('ko') ? 'kr' : 'en';
    }

    init() {
        // Update content immediately on page load
        this.updateContent();

        // Setup language switch after a short delay to ensure navbar is loaded
        setTimeout(() => {
            this.setupLanguageSwitch();
        }, 100);
    }

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('lang', lang);
        this.updateContent();
    }

    updateContent() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[this.currentLang][key]) {
                if (element.tagName === 'INPUT' && element.type === 'submit') {
                    element.value = translations[this.currentLang][key];
                } else {
                    element.textContent = translations[this.currentLang][key];
                }
            }
        });

        // Handle placeholders
        const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            if (translations[this.currentLang][key]) {
                element.placeholder = translations[this.currentLang][key];
            }
        });

        // Handle select options
        const optionElements = document.querySelectorAll('option[data-i18n]');
        optionElements.forEach(option => {
            const key = option.getAttribute('data-i18n');
            if (translations[this.currentLang][key]) {
                option.textContent = translations[this.currentLang][key];
            }
        });
    }

    setupLanguageSwitch() {
        // This will be called when navbar is loaded
        const langSwitch = document.getElementById('lang-switch');
        if (langSwitch) {
            langSwitch.value = this.currentLang;
            langSwitch.addEventListener('change', (e) => {
                this.setLanguage(e.target.value);
            });
        }
    }

    t(key) {
        return translations[this.currentLang][key] || key;
    }
}

